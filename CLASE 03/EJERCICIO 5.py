"""Listas en python"""

""" Listas, pop(i): Quitar un elemento en una posición dada """

paises = ["Perú", "Canadá", "Francia", "México", "Bolivia", "Egipto"]
print("Mi lista inicial: {}".format(paises))

paises.pop()
print("Mi lista actualizada: {}".format(paises))

paises.pop(1)
print("Mi lista removida: {}".format(paises))